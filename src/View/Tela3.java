    private void filtrarAutores(String nome) {

        DefaultTableModel dtm = (DefaultTableModel) Tabela3.getModel();
        dtm.setRowCount(0);

        AutorController autorController = new AutorController();  

        ArrayList<autorModel> listaautor = autorController.buscarAutorController(nome);

          Iterator<autorModel> iterator = listaautor.iterator(); 

        while (iterator.hasNext()) {
            autorModel autor = iterator.next();
            System.out.println("Dados: " + autor);

            dtm.addRow(new Object[]{
                autor.getNome(),
                autor.getNascionalidade()
            });
        }
    } 

