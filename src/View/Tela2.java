    private void alteraRegistro()  {
        if (Tabela2.getSelectedRow() != -1) {
            int resp = JOptionPane.showConfirmDialog(rootPane, "Deseja realmente alterar este registro?",
                    "Confirmação!", JOptionPane.YES_NO_OPTION);

            // Se a confirmação for SIM
            if (resp == JOptionPane.YES_NO_OPTION) {                
                livroModel c = new livroModel(); 
                LivroBancoDeDados d = new LivroBancoDeDados();  
                
                c.setTitulo(txtTitulo.getText());
                c.setGenero(txtGenero.getText());
                c.setPaginas(txtPaginas.getText());
                c.setSinopse(txtSinopse.getText());
                         
                d.alterarlivroBD(c);
                
                
                JOptionPane.showMessageDialog(rootPane, "Registro alterado com sucesso.");
                
               txtTitulo.setText("");
               txtGenero.setText("");
               txtPaginas.setText("");
               txtSinopse.setText("");
              
               txtTitulo.setEnabled(true);
               txtGenero.setEnabled(true);
               txtPaginas.setEnabled(true);
               txtSinopse.setEnabled(true);
               Tabela2.setEnabled(true);
        
                listarlivrosView();
            }
        } else {
            JOptionPane.showMessageDialog(rootPane, "Registro não selecionado.");
        }
    }
    
    
    
    
