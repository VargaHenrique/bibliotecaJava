    //EXcluir - repetir o mesmo processo no cliente
    private void btnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirActionPerformed
        // TODO add your handling code here:

        String titulo = JOptionPane.showInputDialog(null, "Digite o título do livro a ser excluído:");

        if (titulo != null && !titulo.isEmpty()) {
            livroController excluirLivro = new livroController();
            boolean sucesso = excluirLivro.excluirLivro(titulo);

            if (!sucesso) {
                JOptionPane.showMessageDialog(null, "Livro excluído com sucesso!");
                listarlivrosView();
                
            } else {
             JOptionPane.showMessageDialog(null, "Falha ao excluir o livro. Verifique o título fornecido.");
            }
        } else {
            JOptionPane.showMessageDialog(null, "Operação de exclusão cancelada.");
        }

        txtTitulo.setText("");
        txtGenero.setText("");
        txtPaginas.setText("");
        txtSinopse.setText("");
    }//GEN-LAST:event_btnExcluirActionPerformed

